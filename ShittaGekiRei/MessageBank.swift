    // MARK: - Random picker (deduplicated per session)
    static func random(for mode: NotificationMode) -> String {
        let key = "usedMessages_\(mode.rawValue)"
        let all: [String]
        switch mode {
        case .monday:   all = monday
        case .thursday: all = thursday
        case .friday:   all = friday
        }

        var used = (UserDefaults.standard.array(forKey: key) as? [Int]) ?? []
        let unused = all.indices.filter { !used.contains($0) }
        let pick = unused.isEmpty ? Int.random(in: 0..<all.count) : unused.randomElement()!

        if unused.isEmpty {
            used = [pick]
        } else {
            used.append(pick)
        }
        UserDefaults.standard.set(used, forKey: key)

        return all[pick]
    }
